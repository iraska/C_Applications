#include <stdio.h>

int main() 
{
    int numb1, numb2;
    int* ptr1, * ptr2;

    // Assigning pointers to variables
    ptr1 = &numb1;
    ptr2 = &numb2;

    // Value assignment
    *ptr1 = 10;
    *ptr2 = 20;

    // Printing values to the screen
    printf("numb1: %d\n", *ptr1);
    printf("numb2: %d\n", *ptr2);

    return 0;
}
